function l_judge_current_pc(where) {
    var pc = [];

    if (typeof where != 'string' || where == '') {
        return null;
    }

    var i = 0;
    for (var c = _location.length; i < c; i++) {
        if (_location[i][1] > 0) {
            break;
        }
        if (where.indexOf(_location[i][2]) != -1) {
            pc[0] = i;
            break;
        }
    }

    if (typeof(pc[0]) == 'undefined') {
        return null;
    }

    var pid = pc[0] + 1;
    for (var c = _location.length; i < c; i++) {
        if (_location[i][1] == pid && where.indexOf(_location[i][2]) != -1) {
            pc[1] = i;
            break;
        }
    }

    return pc;
}

