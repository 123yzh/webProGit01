function getPageScroll() {
    if (document.documentElement) {
    	t1 = document.documentElement.scrollTop;
    	l1 = document.documentElement.scrollLeft;
    }
    if (document.body) {
    	t2 = document.body.scrollTop;
    	l2 = document.body.scrollLeft;
    }
    return { top: t1 + t2, left: l1 + l2 };
}

function getClient(e) {
	e = (typeof(e) == "object") ? e : document.getElementById(e);
	var h, w, de;
	if (e) {
		w = e.clientWidth;
		h = e.clientHeight;
	} else {
		de = document.documentElement;
		w = window.innerWidth || self.innerWidth || (de&&de.clientWidth) || document.body.clientWidth;
		h = window.innerHeight || self.innerHeight || (de&&de.clientHeight) || document.body.clientHeight;
	}
	return {w:w,h:h};
}

function getPosition(el) {
	el = (typeof(el) == "object") ? el : document.getElementById(el);
	var x = 0, y = 0;
	while(el) {
		x += el.offsetLeft || 0;
		y += el.offsetTop || 0;
		el = el.offsetParent;
	}
	return {x:x, y:y};
}

function getSize(el) {
	el = (typeof(el) == "object") ? el : document.getElementById(el);
	return {
		w:el.offsetWidth||0,
		h:el.offsetHeight||0
	};
}

